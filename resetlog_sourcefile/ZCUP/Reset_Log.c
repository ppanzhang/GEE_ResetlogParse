static void Reset_LogGetCallStack(
uint32 pc,
uint32 sp,
uint32 lr,
uint32 pc_min,
uint32 pc_max,
uint32 sp_min,
uint32 sp_max,
Reset_LogFaultInfoType* Rest_LogFaultInfoPtr)
{
    uint32 nextpc = pc;
    uint32 scan_steps = 4096u;
    uint32 frame_tmp_count = 0;

    /* insert the first frame */
    if(frame_tmp_count < RESET_LOG_CALL_STACK_HIERARCHY)
    {
        Rest_LogFaultInfoPtr->callStack[frame_tmp_count] = pc;
        frame_tmp_count++;
    }
    if(frame_tmp_count >= RESET_LOG_CALL_STACK_HIERARCHY)
    {
        return;
    }
    if(pc < pc_min || pc_max < pc)
    {
        return;
    }

    nextpc = pc+2U;

    while((pc_min <= nextpc) && (nextpc <= pc_max) && (scan_steps > 0U))
    {
        uint16 nextinst = *(uint16*)nextpc;
        if((nextinst & 0xFF00U) == 0xBD00U) /* e.g. BD07 POP r4-r6,pc */ 
        {
            /* found pop instruction */
            uint16 framesize = 4U*(bitcount[nextinst&0x0FU]+bitcount[(nextinst>>4u)&0x0FU]);
            /* only check sp modification if there is no jump before */
            if (nextpc == pc+2U)
            {
                if(pc_min <= pc && pc <= pc_max)
                {
                    uint16 inst = *(uint16*)pc;
                    if((inst & 0xFF80U) == 0xB000) /* e.g. 07B0 SUB sp,#16*/
                    {
                        framesize += 4U*(inst&0x007FU);
                    }
                }
                else
                {
                    /* shall never be here,
                    because pc is calculated by nextpc,
                    and nextpc is already checked in while condition */
                    break;
                }
            }

            sp += framesize;
            if((sp_min <= sp) && (sp <= sp_max))
            {
                /* return address is callee address+3 */
                nextpc = ((*(uint32*)sp)&0xFFFFFFFEU);
                scan_steps = 4096u;

                Rest_LogFaultInfoPtr->callStack[frame_tmp_count] = nextpc;
                frame_tmp_count++;
                if(frame_tmp_count >= RESET_LOG_CALL_STACK_HIERARCHY)
                {
                    break;
                }
            }
            else
            {
                break;
            }
            sp += 4U;
        }
        else if((nextinst & 0xFFFFU) == 0x4770U) /* e.g. 7047   bx  lr */
        {
            /* previous instruction could be a 32bit thumb/arm pop */
            if (nextpc == pc+4U)
            {
                if(pc_min <= pc && pc+2U <= pc_max)
                {
                    uint16 inst1 = *(uint16*)pc;
                    uint16 inst2 = *(uint16*)(pc+2U);
                    if((inst1 & 0xFFFFU) == 0xE8BDU) /* ARM POP */
                    {
                        uint32 framesize = 0U;
                        framesize += 4*bitcount[(inst2>>0U)&0x000FU];
                        framesize += 4*bitcount[(inst2>>4U)&0x000FU];
                        framesize += 4*bitcount[(inst2>>8U)&0x000FU];
                        framesize += 4*bitcount[(inst2>>12U)&0x000FU];

                        sp += framesize;
                        if( inst2 & 0xC000U )
                        {
                            if((sp_min <= sp) && (sp <= sp_max))
                            {
                                lr = *(uint32*)(sp-4U);
                            }
                            else
                            {
                                break;
                            }
                        }
                    }
                }
                else
                {
                    break;
                }
            }
            nextpc = (lr)&0xFFFFFFFEU;
            scan_steps = 4096u;
            Rest_LogFaultInfoPtr->callStack[frame_tmp_count] = nextpc-5;
            frame_tmp_count++;
            if(frame_tmp_count >= RESET_LOG_CALL_STACK_HIERARCHY)
            {
                break;
            }
        }
        else if(((nextinst & 0xE000U) == 0xE000U) && (nextinst & 0x1800U) )
        {
            /* search next 32bit instruction */
            pc = nextpc;
            nextpc += 4U;
            scan_steps--;
        }
        else
        {
            /* search next 16bit instruction */
            pc = nextpc;
            nextpc += 2U;
            scan_steps--;
        }
    }
}

